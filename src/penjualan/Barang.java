    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtIdBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Id masih kosong","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtIdBarang.requestFocus();
        }else if(txtNamaBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama Barang belum diisi","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtNamaBarang.requestFocus();
        }else if(txtJenisBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Jenis Barang belum diisi","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtJenisBarang.requestFocus();
        }else if(txtJumlah.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Jumlah  belum diisi","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtJumlah.requestFocus();
        }else if(txtSatuan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Satuan belum diisi","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtSatuan.requestFocus();
        }else if(txtHarga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harga  belum diisi","Perhatian",
                JOptionPane.WARNING_MESSAGE);
            txtHarga.requestFocus();
        }else{
            try{
                        Object[] options={"Simpan","Lihat Kembali"};
                        int n=JOptionPane.showOptionDialog(null, "<html> Yakin <b><i>"
                            +txtIdBarang.getText()+" "+"</i></b> akan simpan? </html>", "Konfirmasi",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.QUESTION_MESSAGE,null,options,options[0]);
                        if(n==javax.swing.JOptionPane.YES_OPTION){
                            query.InputDetail(Kolom(), Data(), "barang");
                            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                            bersih();
                }
            }catch(Exception exc){
                System.err.println(exc.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

