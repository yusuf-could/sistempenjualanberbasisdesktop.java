    private void setIdBarang(){
        {
            try{
                koneksi knks = new koneksi();
                Connection con = knks.bukakoneksi();
                Statement st = con.createStatement();
                String sql;
                sql="SELECT id_barang FROM barang ORDER BY id_barang desc";
                ResultSet rs=st.executeQuery(sql);
                
                if(rs.next()){
                    String IdBarang = rs.getString ("id_barang").substring(3);
                    String no = "" + (Integer.parseInt(IdBarang) + 1);
                    String nol = "";
                    
                    if (no.length()==1)
                    {nol = "000";}
                    else if (no.length()==2)
                    {nol = "00";}
                    else if (no.length()==3)
                    {nol = "0";}
                    else if (no.length()==4)
                    {nol = "";}
                    txtIdBarang.setText("ID-" + nol + no);
                }else {
                    txtIdBarang.setText("ID-0001");
                }
            }catch(Exception exc)
            {exc.printStackTrace();}
        }
           
    }
